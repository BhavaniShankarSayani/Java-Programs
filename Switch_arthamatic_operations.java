class Switch_arthamatic_operations
   {
public static void main(String[]args) {
    int n1=20,n2=30;
    char a='+';
    switch(a) {
	case '+': System.out.println(n1+n2);
          break;

    	case '-': System.out.println(n1-n2);
          break;

    	case '/': System.out.println(n1/n2);
          break;

       
	case '*': System.out.println(n1*n2);
          break;
	default:

       }
    }
}

class Roman_numbers 
   {
public static void main(String[]args) {
     	char a='5';
	if(a>='0' && a<='9') 
	switch(a) {

	case '0': System.out.println("it is 0 "+a);
	break;

	case '1': System.out.println("it is I "+a);
	break;

	case '2': System.out.println("it is II "+a);
	break;

	case '3': System.out.println("it is III "+a);
	break;

	case '4': System.out.println("it is IV "+a);
	break;

	case '5': System.out.println("it is V "+a);
	break;

	case '6': System.out.println("it is VI "+a);
	break;

	case '7': System.out.println("it is VII "+a);
	break;

	case '8': System.out.println("it is VIII "+a);
	break;

	case '9': System.out.println("it is XI "+a);
	break;

	default:
     }
   }
}
